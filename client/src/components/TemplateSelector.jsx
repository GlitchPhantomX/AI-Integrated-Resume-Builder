import React, { useState } from "react";
import { Check, Layout } from "lucide-react";
import { templates } from "../data/componentsData";
import { ScrollArea } from "../ui/scroll-area";
// import { Separator } from "@/components/ui/separator"; // optional if you want separators

const TemplateSelector = ({ selectedTemplate, onChange }) => {
  const [isOpen, setIsOpen] = useState(false);

  return (
    <div className="relative">
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="flex items-center gap-1 text-sm text-blue-600 bg-gradient-to-br from-blue-50 to-blue-100 ring-blue-300 hover:ring transition-all px-3 py-2 rounded-lg"
      >
        <Layout size={14} />
        <span className="max-sm:hidden">Template</span>
      </button>

      {isOpen && (
        <div className="absolute top-full left-0 w-64 mt-2 z-50 bg-white rounded-md border border-gray-200 shadow-lg">
          {/* ðŸ‘‡ ScrollArea Wrap */}
          <ScrollArea className="h-72 p-3">
            <div className="space-y-3">
              {templates.map((template) => (
                <div
                  key={template.id}
                  className={`relative p-3 border rounded-md cursor-pointer transition-all ${
                    selectedTemplate === template.id
                      ? "border-blue-400 bg-blue-100"
                      : "border-gray-300 hover:border-gray-400 hover:bg-gray-100"
                  }`}
                  onClick={() => {
                    onChange(template.id);
                    setIsOpen(false);
                  }}
                >
                  {selectedTemplate === template.id && (
                    <div className="absolute top-2 right-2">
                      <div className="size-5 bg-blue-400 rounded-full flex items-center justify-center">
                        <Check className="w-3 h-3 text-white" />
                      </div>
                    </div>
                  )}
                  <div className="space-y-1">
                    <h4 className="font-medium text-gray-800">
                      {template.name}
                    </h4>
                    <div className="mt-2 p-2 bg-blue-50 rounded text-xs text-gray-500 italic">
                      {template.preview}
                    </div>
                  </div>
                  {/* Optional Separator */}
                  {/* <Separator className="my-2" /> */}
                </div>
              ))}
            </div>
          </ScrollArea>
        </div>
      )}
    </div>
  );
};

export default TemplateSelector;
